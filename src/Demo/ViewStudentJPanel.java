    public void BindStudents(){
        try{
           DBAccess db = new DBAccess();
           drpdnStudent.removeAllItems();
           
            for(Student student : db.fetchStudents()){
                drpdnStudent.addItem(student);
            }
       }
       catch(Exception e){
           System.out.println(e);
       }
    }
    
