    private void drpdnStudentItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_drpdnStudentItemStateChanged
        // TODO add your handling code here:
        if(evt.getStateChange() == ItemEvent.SELECTED){
            System.out.println("drop down Category state change function called for Student...");
            Student objStudent = (Student)drpdnStudent.getSelectedItem();

            if(objStudent != null){
                ClearJLables();
                
                lblStudentId.setText(String.valueOf(objStudent.getStudentId()));
                lblLastName.setText(objStudent.getLastName());
                lblFathersName.setText(objStudent.getFatherName());
                lblMotherName.setText(objStudent.getMotherName());
                lblAgeCategory.setText(objStudent.getAgeCategory().getName());
                lblRegistrationDate.setText(String.valueOf(objStudent.getRegistrationDate()));
                lblTeacherName.setText(objStudent.getTeacher().getName());
                lblClassRoomAssigned.setText(String.valueOf(objStudent.getTeacher().getClassroomAssigned())); // Need to change  this
            }
        }
    }//GEN-LAST:event_drpdnStudentItemStateChanged

