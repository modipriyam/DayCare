    private void drpdnAgeCategoryItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_drpdnAgeCategoryItemStateChanged
        // TODO add your handling code here:
        if(evt.getStateChange() == ItemEvent.SELECTED){
            System.out.println("drop down Category state change function called...");
            Category objCategory = (Category)drpdnAgeCategory.getSelectedItem();

            if(objCategory != null){
                 bindTeachers(objCategory.getId());
            }
        }
        
    }//GEN-LAST:event_drpdnAgeCategoryItemStateChanged

