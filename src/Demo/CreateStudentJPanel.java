    // Function to bind Categories
    public void bindCategories(){
       
       try{
           DBAccess db = new DBAccess();
           drpdnAgeCategory.removeAllItems();
           
            for(Category category : db.fetchCategories()){
                drpdnAgeCategory.addItem(category);
            }
       }
       catch(Exception e){
           System.out.println(e);
       }
    }
    
    // Function to bind Teacher
    public void bindTeachers(int categoryId){
       
       try{
           DBAccess db = new DBAccess();
           drpdnTeacher.removeAllItems();
           
            for(Teacher teacher : db.fetchTeachers(categoryId)){
                drpdnTeacher.addItem(teacher);
            }
       }
       catch(Exception e){
           System.out.println(e);
       }
    }

