    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
     try{
        Student objStudent = new Student();
        objStudent.setFirstName(txtFirstName.getText());
        objStudent.setLastName(txtLastName.getText());
        objStudent.setFatherName(txtFatherName.getText());
        objStudent.setMotherName(txtMotherName.getText());
        objStudent.setAgeCategory((Category)drpdnAgeCategory.getSelectedItem());
        objStudent.setTeacher((Teacher)drpdnTeacher.getSelectedItem());
        objStudent.setNotes(txtNotes.getText());
        Date dateOfBirth = new SimpleDateFormat(dateFormat).parse(txtDOB.getText());
        Date dateOfRegistration = new SimpleDateFormat(dateFormat).parse(txtRegistrationDate.getText());

        objStudent.setDateOfBirth(dateOfBirth);
        objStudent.setRegistrationDate(dateOfRegistration);
        System.out.println("Printing Student DOB"+ dateOfBirth);
        System.out.println(objStudent.getDateOfBirth());
        // Adding student data to Database
        DBAccess db = new DBAccess();
        db.addStudent(objStudent);
        
        // Navigate to student option screen with confirmation msg
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        StudnetJPanel student = new StudnetJPanel(userProcessContainer, "Student Details Updated !!");
        userProcessContainer.add("Student", student);
        layout.next(userProcessContainer);
        
       }
       catch(Exception e){
           System.out.println(e);
       }
    }//GEN-LAST:event_btnSubmitActionPerformed

